library(sets)
library(ggplot2)
mf1 <- genmf('trapmf', c(10,20,30,50))
mf2 <- genmf('trapmf', c(30,50,70,95))
trap1 <- evalmf(0:100, mf1)
trap2 <- evalmf(0:100, mf2)
plot(trap1, type="l", col="red")
lines(trap2, type="l", col="darkgreen")

